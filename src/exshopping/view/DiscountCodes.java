    private void add_btnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_add_btnMouseClicked
        // TODO add your handling code here:
 String code = discount_code_input.getText().trim();
		String percentageStr = percentage_input.getText().trim();
		String maxUsageStr = max_usage_input.getText().trim();

		// Validate discount code
		if (code.isEmpty()) {
			JOptionPane.showMessageDialog(this, "Discount code cannot be empty", "Validation Error", JOptionPane.ERROR_MESSAGE);
			discount_code_input.requestFocus();
			return;
		}

		// Validate percentage
		double percentage;
		try {
			percentage = Double.parseDouble(percentageStr);
			if (percentage <= 0 || percentage > 100) {
				JOptionPane.showMessageDialog(this, "Percentage must be between 0 and 100", "Validation Error", JOptionPane.ERROR_MESSAGE);
				percentage_input.requestFocus();
				return;
			}
		} catch (NumberFormatException e) {
			JOptionPane.showMessageDialog(this, "Invalid percentage value", "Validation Error", JOptionPane.ERROR_MESSAGE);
			percentage_input.requestFocus();
			return;
		}

		// Validate max usage
		int maxUsage;
		try {
			maxUsage = Integer.parseInt(maxUsageStr);
			if (maxUsage <= 0) {
				JOptionPane.showMessageDialog(this, "Max usage must be a positive number", "Validation Error", JOptionPane.ERROR_MESSAGE);
				max_usage_input.requestFocus();
				return;
			}
		} catch (NumberFormatException e) {
			JOptionPane.showMessageDialog(this, "Invalid max usage value", "Validation Error", JOptionPane.ERROR_MESSAGE);
			max_usage_input.requestFocus();
			return;
		}

		// Attempt to create discount code
		boolean discountCreated = DiscountController.createDiscountCode(code, percentage, maxUsage);

		if (discountCreated) {
			JOptionPane.showMessageDialog(this, "Discount code created successfully", "Success", JOptionPane.INFORMATION_MESSAGE);

			// Clear input fields after successful creation
			clearInputFields();
			populateDiscountTable();
		} else {
			JOptionPane.showMessageDialog(this, "Failed to create discount code", "Error", JOptionPane.ERROR_MESSAGE);
		}
    }//GEN-LAST:event_add_btnMouseClicked

    private void remove_btnMouseExited(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_remove_btnMouseExited
        // TODO add your handling code here:
		String code_text = discount_code_input.getText();

		if(code_text.isEmpty()) {
			JOptionPane.showMessageDialog(this, "Failed to create discount code", "Error", JOptionPane.ERROR_MESSAGE);
			return;
		}

		if(DiscountController.deleteDiscountCode(code_text)) {
			JOptionPane.showMessageDialog(this, "Sucessfully removed!!", "Success", JOptionPane.INFORMATION_MESSAGE);
			clearInputFields();
			populateDiscountTable();
		}else {
			JOptionPane.showMessageDialog(this, "Sucessfully removed!!", "Error", JOptionPane.ERROR_MESSAGE);
		}

    }//GEN-LAST:event_remove_btnMouseExited

