	private void populateDiscountTable() {
		List<DiscountCode> discountCodes = getAllDiscountCodes();

		// Define column names
		String[] columnNames = {"Discount Code", "Percentage", "Max Usage", "Current Usage"};

		// Create a default table model
		DefaultTableModel model = new DefaultTableModel(columnNames, 0);

		// Populate the model with data
		for (DiscountCode code : discountCodes) {
			Object[] rowData = {
				code.getCode(),
				code.getPercentage() + "%",
				code.getMaxUsage(),
				code.getCurrentUsage()
			};
			model.addRow(rowData);
		}

		// Set the model to the table
		discount_table.setModel(model);
	}

